# Operadores aritmeticos

num1 = 10
num2 = 5

# suma

resultado = num1 + num2

print(resultado)

# resta

resultado = num1 - num2

print(resultado)

# multiplicacion

resultado = num1 * num2

print(resultado)

# division

resultado = num1 / num2

print(resultado)

# division entera

resultado = num1 // num2

print(resultado)

# modulo

resultado = num1 % num2

print(resultado)

# Exponente

resultado = num1 ** num2

print(resultado)

# ejercicio

resultado = 3**3 * (13/5-(2*4))
print(resultado)