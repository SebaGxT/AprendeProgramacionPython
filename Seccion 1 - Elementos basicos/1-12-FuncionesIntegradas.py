# Funciones integradas - Pertenecen al lenguaje python y sirven para hacer conversiones entre tipos de datos

# Conversion a entero
n = int('10')
print(type(n))

# Conversion a Float
n = float('10.8')
print(type(n))

# Conversion a cadena
n = str(10.98)
print(type(n))

# Conversion a binario
n = bin(10)
print(n)

# Conversion a Hexadecimal
n = hex(10)
print(n)

# Binario a Entero
n = int('0b1010',2)
print(n)

# Hexadecimal a Entero
n = int("0xa",16)
print(n)

# Valor absoluto
n = abs(-8)
print(n)

# Redondeo
n = round(5.6)
print(n)

n = round(5.4)
print(n)

# tama√±o de cadena
n = len('Sebastian')
print(n)
